import turtle

turtle.penup()
turtle.goto(-200, 0)
turtle.pendown()

turtle.write("I", font=("Arial", 24, "bold"))
turtle.penup()
turtle.forward(40)
turtle.pendown()

turtle.write("love", font=("Arial", 24, "bold"))
turtle.penup()
turtle.forward(70)
turtle.pendown()

turtle.write("Rebecca", font=("Arial", 24, "bold"))

turtle.done()
